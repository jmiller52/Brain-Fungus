# Basic for loop usage with lists
# loops require the colon and the following lines to be indented.
# Once done with the actions needed for the loop return to the unindented line and keep going... or not.

# 4-1 Pizzas
print('Exercise 4-1 Pizzas\n')
pizzas = ['pepperoni', 'sausage', 'ham']
for pizza in pizzas:
    print(f'I like {pizza.title()}.')

print('Pizza is one of my favorite foods to eat.')

print('\nExercise 4-2 Animals\n')
# 4-2 Animals
animals = ['dog', 'pig', 'bird']
for animal in animals:
    print(f'A {animal} would make a good pet.')

print('All of these animals are would make a better pet than a cat.')